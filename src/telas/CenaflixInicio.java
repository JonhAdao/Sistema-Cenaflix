    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (!emptyFields2()) {
            try {
                String nome = txtNome.getText();
                String data = txtData.getText();
                String categoria = boxCategoria.getSelectedItem().toString();
                int id = Integer.parseInt(txtId.getText());

                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                Date Data = formato.parse(data);
                Filmes filme = new Filmes();
                filme.setNome(nome);
                filme.setData(Data);
                filme.setCategoria(categoria);
                filme.setId(id);

                FilmesDao filmes = new FilmesDao();

                filmes.editarFilme(filme);

                txtId.setText("");
                txtNome.setText("");
                txtData.setText("");
                boxCategoria.setSelectedIndex(0);
            } catch (ParseException ex) {
                System.out.println("Erro de conversão " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        if (!emptyFields()) {
            if (!validarCampo()) {

                try {
                    String nomeFilme = txtNome.getText();
                    String categoria = boxCategoria.getSelectedItem().toString();
                    String dataLanc = txtData.getText();
                    Filmes filme = new Filmes();

                    SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

                    filme.setNome(nomeFilme);
                    filme.setData(formato.parse(dataLanc));
                    filme.setCategoria(categoria);

                    FilmesDao filmeDao = new FilmesDao();
                    filmeDao.salvarFilmes(filme);

                    txtNome.setText("");
                    txtData.setText("");
                    boxCategoria.setSelectedIndex(0);

                } catch (ParseException ex) {
                    System.out.println("Erro de conversão: " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

