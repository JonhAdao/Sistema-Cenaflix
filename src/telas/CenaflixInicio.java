    /**
     * Método para validar campo de data ao inserir
     */
    private boolean validarCampo() {
        boolean validado = true;

        boolean dataValida = txtData.getText().matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}");

        if (dataValida == false) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! A data tem que ser no formato: 04/02/1993");
            txtData.requestFocus();

        } else {
            JOptionPane.showMessageDialog(rootPane, "Dados incluidos com sucesso");
            validado = false;
        }

        return validado;
    }

    /**
     * Método para verificar se os campos não estão vazios
     */
    private boolean emptyFields() {

        boolean vazio = true;

        if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Nome não pode ser vazio.");
            txtNome.requestFocus();

        } else if (txtData.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Data não pode ser vazio.");
            txtData.requestFocus();

        } else {
            vazio = false;
        }
        return vazio;
    }

    /**
     * Método para verificar se id de consulta não esta vazio (exibe mensagem
     * diferente)
     */
    private boolean idVazio() {
        boolean vazio = true;

        if (txtIdPesquisa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Digite um ID válido para retornar a consulta");
            txtIdPesquisa.requestFocus();

        } else {
            vazio = false;
        }
        return vazio;
    }

    /**
     * Método para instruir usuário á fazer uma consulta antes de editar algum
     * dado
     */
    private boolean emptyFields2() {

        boolean vazio = true;

        if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! Faça uma consulta para trazer os dados para atualizar");
            txtIdPesquisa.requestFocus();

        } else {
            vazio = false;
        }
        return vazio;
    }

