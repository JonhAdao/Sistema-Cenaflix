    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        if (!idVazio()) {
            int idPesquisa = Integer.parseInt(txtIdPesquisa.getText());

            FilmesDao filmesDAO = new FilmesDao();
            Filmes filme = filmesDAO.consultarFilme(idPesquisa);

            if (filme == null) {
                JOptionPane.showMessageDialog(this, "Filme não encontrado!");
            } else {
                txtId.setText(String.valueOf(filme.getId()));
                txtNome.setText(filme.getNome());
                txtData.setText(filme.getData().toString());
                boxCategoria.setSelectedItem(filme.getCategoria());

            }
            txtIdPesquisa.setText("");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

